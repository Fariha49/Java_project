    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed
        
         String ProductName=ProName.getText();
         String ID=ProductID.getText();
         String Serial=SerialVar.getText();
         String Warrent=Warrenty.getText();
         String PriceRate=SalePriceRate.getText();
         String Quantity=SaleQuantity.getText();
         String VAT=VATVar.getText();
         String Total=TotalVar.getText();
         String Customer=CusNamVar.getText();
         String Contact=ContactVar.getText();
         String Address=AddressVar.getText();
         String Payment=PaymentVar.getText();
         String Date=DateVar.getText();
         String home=Home.getText();

        try{
            String sql = "INSERT INTO Bata(Product_Name,Product_ID,Serial_Number,Warrenty,Sale_Price_Rate,Sale_Quantity,VAT,Item_Total_Prize,Customer_Name,Contact,Address,Payment,Date)  VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?)";
            ps=conn.prepareStatement(sql);
            
            ps.setString(1 ,ProductName);
            ps.setString(2 , ID);
            ps.setString(3 ,Serial);
            ps.setString(4 ,Warrent);
            ps.setString(5 ,PriceRate);
            ps.setString(6 ,Quantity);
            ps.setString(7 ,VAT);
            ps.setString(8 ,Total);
            ps.setString(9 ,Customer);
            ps.setString(10 ,Contact);
            ps.setString(11 , Address);
            ps.setString(12 , Payment);
            ps.setString(13 ,Date);
              ps.setString(14 ,home);
            
            ps.execute();
                     JOptionPane.showMessageDialog(null, "Saved");
                 ps.close();
                    
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
       
   
    }//GEN-LAST:event_InsertActionPerformed

